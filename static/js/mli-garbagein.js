/* Garbage In, Garbage Out — a real softmax classifier trained live in the browser.
   Training data is generated by augmenting the ten prototype digits bundled with
   TinyDigits (random shifts + noise), so we get a genuine train/test split with no
   external dataset. A slider corrupts a fraction of the TRAINING labels; the model
   is retrained and its accuracy on a clean test set visibly collapses. */
window.MliGarbageIn = (function () {
  var S = 28, D = 14, F = D * D, K = 10;
  var N_TRAIN = 60, N_TEST = 25, EPOCHS = 35, LR = 0.15, SIGMA = 0.18;

  var _sp = null;
  function gauss() {
    if (_sp !== null) { var t = _sp; _sp = null; return t; }
    var u = Math.random() || 1e-9, v = Math.random(), r = Math.sqrt(-2 * Math.log(u));
    _sp = r * Math.sin(2 * Math.PI * v); return r * Math.cos(2 * Math.PI * v);
  }
  // 2x2 average-pool a 28x28 prototype to 14x14, with an integer shift and optional noise.
  function makeVec(g28, dx, dy, sigma) {
    var out = new Float64Array(F);
    for (var y = 0; y < D; y++) for (var x = 0; x < D; x++) {
      var s = 0;
      for (var yy = 0; yy < 2; yy++) for (var xx = 0; xx < 2; xx++) {
        var sy = y * 2 + yy - dy, sx = x * 2 + xx - dx;
        if (sy >= 0 && sy < S && sx >= 0 && sx < S) s += g28[sy * S + sx];
      }
      var vv = s / 4;
      if (sigma) vv += sigma * gauss();
      out[y * D + x] = vv < 0 ? 0 : (vv > 1 ? 1 : vv);
    }
    return out;
  }

  function init(root, TD) {
    if (!root || !TD) return;
    var corrEl = root.querySelector("#mli-gi-corr");
    var corrV = root.querySelector("#mli-gi-corrv");
    var btn = root.querySelector("#mli-gi-train");
    var accEl = root.querySelector("#mli-gi-acc");
    var fill = root.querySelector("#mli-gi-fill");
    var grid = root.querySelector("#mli-gi-grid");
    var cap = root.querySelector("#mli-gi-cap");
    if (!corrEl || !accEl) return;

    // Build prototypes and a fixed train/test split (augmentations differ per set).
    var protos = [];
    for (var c = 0; c < 10; c++) {
      var img = TD.sampleImg(c), g = new Float64Array(S * S);
      for (var i = 0; i < S * S; i++) g[i] = img[i] / 255;
      protos.push(g);
    }
    function gen(nPer) {
      var X = [], Y = [];
      for (var c = 0; c < 10; c++) for (var k = 0; k < nPer; k++) {
        var dx = (Math.random() * 5 | 0) - 2, dy = (Math.random() * 5 | 0) - 2;
        X.push(makeVec(protos[c], dx, dy, SIGMA)); Y.push(c);
      }
      return { X: X, Y: Y };
    }
    var tr = gen(N_TRAIN), te = gen(N_TEST);

    // Pick a compact, class-balanced set of test digits to display (2 per class).
    var show = [];
    for (var c2 = 0; c2 < 10; c2++) { var seen = 0; for (var i2 = 0; i2 < te.X.length && seen < 2; i2++) { if (te.Y[i2] === c2) { show.push(i2); seen++; } } }

    // Pre-build the thumbnail canvases once.
    var fg = "17,17,17";
    try { var cv = getComputedStyle(document.documentElement).getPropertyValue("--c-fg").trim(); if (cv) fg = cv.replace(/\s+/g, ","); } catch (e) {}
    var cells = [];
    grid.innerHTML = "";
    for (var s = 0; s < show.length; s++) {
      var cell = document.createElement("div"); cell.className = "mli-gi-cell";
      var cvs = document.createElement("canvas"); cvs.width = 34; cvs.height = 34; cvs.className = "mli-gi-thumb";
      var ctx = cvs.getContext("2d");
      var vec = te.X[show[s]], px = 34 / D;
      if (ctx) for (var y = 0; y < D; y++) for (var x = 0; x < D; x++) {
        var val = vec[y * D + x]; if (val <= 0.03) continue;
        ctx.fillStyle = "rgba(" + fg + "," + val.toFixed(3) + ")";
        ctx.fillRect(x * px, y * px, Math.ceil(px), Math.ceil(px));
      }
      var lab = document.createElement("div"); lab.className = "mli-gi-plab"; lab.textContent = "?";
      cell.appendChild(cvs); cell.appendChild(lab); grid.appendChild(cell);
      cells.push({ cell: cell, lab: lab, idx: show[s] });
    }

    function train(corr) {
      var W = new Float64Array(F * K), b = new Float64Array(K);
      var Y = tr.Y, X = tr.X, N = X.length;
      var YT = Y.slice();
      for (var i = 0; i < N; i++) if (Math.random() < corr) { var r; do { r = Math.random() * K | 0; } while (r === Y[i]); YT[i] = r; }
      var order = []; for (var q = 0; q < N; q++) order.push(q);
      var logit = new Float64Array(K);
      for (var e = 0; e < EPOCHS; e++) {
        for (var a = N - 1; a > 0; a--) { var j = Math.random() * (a + 1) | 0; var tmp = order[a]; order[a] = order[j]; order[j] = tmp; }
        for (var oi = 0; oi < N; oi++) {
          var ix = order[oi], x = X[ix], mx = -1e9, k, f;
          for (k = 0; k < K; k++) { var acc = b[k]; for (f = 0; f < F; f++) acc += W[f * K + k] * x[f]; logit[k] = acc; if (acc > mx) mx = acc; }
          var sum = 0; for (k = 0; k < K; k++) { logit[k] = Math.exp(logit[k] - mx); sum += logit[k]; }
          for (k = 0; k < K; k++) logit[k] /= sum;
          var t = YT[ix];
          for (k = 0; k < K; k++) { var gk = LR * (logit[k] - (k === t ? 1 : 0)); b[k] -= gk; for (f = 0; f < F; f++) W[f * K + k] -= gk * x[f]; }
        }
      }
      return { W: W, b: b };
    }
    function predict(M, x) {
      var arg = 0, mx = -1e9;
      for (var k = 0; k < K; k++) { var acc = M.b[k]; for (var f = 0; f < F; f++) acc += M.W[f * K + k] * x[f]; if (acc > mx) { mx = acc; arg = k; } }
      return arg;
    }
    function testAcc(M) { var ok = 0; for (var i = 0; i < te.X.length; i++) if (predict(M, te.X[i]) === te.Y[i]) ok++; return ok / te.X.length; }

    function run() {
      var corr = (+corrEl.value) / 100;
      if (corrV) corrV.textContent = Math.round(corr * 100) + "%";
      if (btn) { btn.disabled = true; btn.textContent = "Training…"; }
      setTimeout(function () {
        var M = train(corr);
        var a = testAcc(M);
        accEl.textContent = Math.round(a * 100) + "%";
        if (fill) { fill.style.width = Math.round(a * 100) + "%"; fill.classList.toggle("mli-bad", a < 0.6); }
        for (var s = 0; s < cells.length; s++) {
          var p = predict(M, te.X[cells[s].idx]), truth = te.Y[cells[s].idx], right = p === truth;
          cells[s].lab.textContent = p;
          cells[s].cell.classList.toggle("is-wrong", !right);
          cells[s].cell.classList.toggle("is-right", right);
        }
        if (cap) cap.textContent = corr === 0
          ? "Clean labels: the classifier learns the digits and gets almost every test image right."
          : "With " + Math.round(corr * 100) + "% of training labels randomized, the very same model, same architecture, same amount of training, now gets " + Math.round(a * 100) + "% right. The only thing that changed was the data.";
        if (btn) { btn.disabled = false; btn.textContent = "Retrain"; }
      }, 20);
    }

    corrEl.addEventListener("change", run);
    if (btn) btn.addEventListener("click", run);
    run();
  }

  return { init: init };
})();
